def soma(a,b):
    return a+b

def subtracao(a,b):
    return a-b
def multiplicacao(a,b):
    return a*b
def divisao(a,b):
    return a/b

print(soma(10,5))
print(subtracao(10,8))
print(multiplicacao(5,12))
print(divisao(25,5))